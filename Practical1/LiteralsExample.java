public class LiteralsExample {
    public static void main(String[] args) {
        // Floating-point literals
        double normalFloat = 3.14;
        double exponentialFloat = 2.5e-3;
        System.out.println("Normal Floating-Point Literal: " + normalFloat);
        System.out.println("Exponential Float: " + exponentialFloat);

        // Boolean literals
        boolean trueLiteral = true;
        boolean falseLiteral = false;
        System.out.println("Boolean True Literal: " + trueLiteral);
        System.out.println("Boolean False Literal: " + falseLiteral);

        // String literals with escape sequences
        String escapeLiteral = "Hello\tWorld!\n";
        System.out.println("String Literal with Escape Sequences: " + escapeLiteral);
    }
}
