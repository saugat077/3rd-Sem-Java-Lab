        public class BitiwiseOperators {
            public static void main(String[] args) {
                // Bitwise AND operator (&)
                int a = 5; // 101
                int b = 3; // 011
                int c = a & b; // 001
        
                System.out.println("a & b = " + c);
        
                // Bitwise OR operator (|)
                int d = a | b; // 111
        
                System.out.println("a | b = " + d);
        
                // Bitwise XOR operator (^)
                int e = a ^ b; // 110
        
                System.out.println("a ^ b = " + e);
            }
        }
    
